package en.codegym.task.jdk13.task04.task0438;

/* 
Drawing lines
*/

public class Solution {
    public static void main(String[] args) {
        for (int i = 0; i < 1; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.print("8");
            }
            System.out.print("\n");
            for (int j = 0; j < 10; j++) {
                System.out.println("8");
            }
        }
    }
}
